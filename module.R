increment_numbers = function(num_1 ,num_2) {
    # increment the two provided numbers

    # Args:
    #     num_1 (float): first number to increment
    #     num_2 (float): second number to increment

    # Returns:
    #     vector: incremented numbers with form (num_1 + 1, num_2 + 1)
    
    numbers <- c(num_1 + 1, num_2 + 1)
    return(numbers)
}
